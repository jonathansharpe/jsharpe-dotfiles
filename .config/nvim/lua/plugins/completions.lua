	-- 	end,
	-- },
	{
		-- automatically pairs brackets 'n stuff
		"altermo/ultimate-autopair.nvim",
		event = {
			"InsertEnter",
			"CmdlineEnter",
		},
		branch = "v0.6",
		opts = {
			-- config goes here
		},
	},
}
